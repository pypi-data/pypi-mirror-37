def face():
    #右眼睛
    t.pencolor('black')
    t.pensize(5)
    t.penup()
    t.goto(55,15)
    t.pendown()
    t.setheading(110)
    t.circle(20,140)
    t.penup()
    #左眼睛
    t.goto(-55,15)
    t.pendown()
    t.setheading(70)
    t.circle(-20,140)
    t.penup()
    #嘴巴(外)
    t.goto(-15,-10)
    t.pensize(1)
    t.pencolor('coral')
    t.fillcolor('coral')
    t.setheading(-90)
    t.pendown()
    t.begin_fill()
    t.circle(15,180)
    for i in range(10):
        t.forward(3)
        t.left(7)
    t.left(20)
    t.backward(2)
    t.left(20)
    for i in range(10):
        t.forward(3)
        t.left(7)
    t.end_fill()
    #嘴巴(里)
    t.penup()
    t.goto(12,-10)
    t.setheading(120)
    t.pencolor('darkred')
    t.fillcolor('darkred')
    t.begin_fill()
    t.circle(14,120)
    t.setheading(-60)
