import turtle

t = turtle.Pen()
t.speed(0)

def head():
    t.penup()
    t.goto(0, (-100))
    t.pendown()
    t.begin_fill()
    t.fillcolor('gold')
    t.circle(100)
    t.end_fill()
    t.penup()

